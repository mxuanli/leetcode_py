from collections import defaultdict, Counter


class Solution:
    def topKFrequent(self, words: list, k: int) -> list:
        c = Counter(words)
        words_dict = defaultdict(list)
        for key, v in c.items():
            words_dict[v].append(key)
        r = list()
        for key in sorted(words_dict.keys(), reverse=True):
            r += sorted(words_dict[key])
        return r[:k]


